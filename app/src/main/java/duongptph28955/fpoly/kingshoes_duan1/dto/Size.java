package duongptph28955.fpoly.kingshoes_duan1.dto;

public class Size {
    public int maSize;
    public String size;

    public Size() {
    }

    public Size(int maSize, String size) {
        this.maSize = maSize;
        this.size = size;
    }

    public int getMaSize() {
        return maSize;
    }

    public void setMaSize(int maSize) {
        this.maSize = maSize;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
}
